package pkg12.math.pkgclass;

public class MathClass {

    public static void main(String[] args) {
       
        
        System.out.println(Math.abs(-5.4));
        System.out.println(Math.sqrt(25));
        System.out.println(Math.pow(4, 2));
        System.out.println(Math.PI);
        System.out.println(Math.log(4));
        System.out.println(Math.exp(2));
        System.out.println(Math.max(8, 65));
        System.out.println(Math.min(-65, 1));
        System.out.println(Math.ceil(-6.6));
        System.out.println(Math.floor(5.2));
        System.out.println(Math.sin(58));
    }
    
}
