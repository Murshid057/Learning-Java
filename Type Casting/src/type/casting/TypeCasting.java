package type.casting;

public class TypeCasting {

    public static void main(String[] args) {
        
        double y = 11.2;          // Explicit type_casting
        int x = (int) y;
        System.out.println(x);
        
        int a = 12;               // Emplicit type_casting
        float b = a;
        System.out.println(b);
    }
    
}
